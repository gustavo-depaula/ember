#!/usr/bin/env node
// Wraps both vendored and in-house WebView scripts into TS modules.
//
//   paginator.raw.js  → paginatorScript.ts   (MIT, vendored foliate-js;
//                                              patched in-place via .replace
//                                              for snappier paging + GPU layer)
//   bootstrap.raw.js  → bootstrapScript.ts   (our IIFE that wires foliate +
//                                              highlights + selection + taps)
//
// Both files are loaded as classic `<script>` tags inside the WebView host
// HTML emitted by FoliateReader.tsx. Re-run after editing either:
//
//   node bundle.mjs

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))

// ---------------------------------------------------------------------------
// paginatorScript.ts — vendored foliate-js paginator + our patches
// ---------------------------------------------------------------------------

const paginatorRaw = readFileSync(join(here, 'paginator.raw.js'), 'utf8')

// Strip ES-module syntax so the script can run as a classic `<script>` inside
// a WebView whose document is `about:blank`. Module scripts get "Script error.
// @0" with CORS masking; classic scripts just run. The only top-level export
// is `export class Paginator`, and the side effect we actually want is the
// `customElements.define(...)` at the bottom, so dropping the `export` is
// sufficient.
//
// We also patch #onTouchMove to bail out (without calling preventDefault) when
// iOS native text selection is active — otherwise every drag of the selection
// handle calls scrollBy and the page wiggles. The line we anchor to is unique
// (the early-return guard for scrolled/pinched mode). Selection-active = a
// non-collapsed Range exists in the iframe's document.
const paginatorSrc = paginatorRaw
  .replace(/^export\s+class\b/m, 'class')
  .replace(
    'if (this.scrolled || state.pinched) return',
    "if (this.scrolled || state.pinched) return; const __sel = e.target && e.target.ownerDocument && e.target.ownerDocument.getSelection && e.target.ownerDocument.getSelection(); if (__sel && __sel.type === 'Range' && !__sel.isCollapsed) return",
  )
  // Snappier page-turn animation — foliate ships 300ms; Apple Books and the
  // iOS short-transition standard feel closer to 200ms. Anchor on the unique
  // 3-arg slot of the `animate(...)` call inside the page-scroll path.
  .replace(
    'element[scrollProp], offset, 300, easeOutQuad',
    'element[scrollProp], offset, 200, easeOutQuad',
  )
  // Promote #container to a compositor layer so JS-driven scrollLeft writes
  // are GPU-accelerated. Without a layer hint, WebKit re-lays-out the
  // multi-column content on every requestAnimationFrame frame and the page
  // turn reads as low-fps even at the correct duration. `will-change:
  // scroll-position` is the canonical hint; backstop with translateZ(0) for
  // older WebKit versions that don't act on the hint.
  .replace(
    '#container {\n            grid-column: 2 / 5;\n            grid-row: 2;\n            overflow: hidden;\n        }',
    '#container {\n            grid-column: 2 / 5;\n            grid-row: 2;\n            overflow: hidden;\n            will-change: scroll-position;\n            transform: translateZ(0);\n        }',
  )

const paginatorOut = `// biome-ignore-all lint/suspicious/noTemplateCurlyInString: vendored foliate-js — \${…} sequences run inside the WebView, not in this TS module.
// biome-ignore-all format: single-line embedded blob; reformatting would split it across thousands of lines.
/**
 * Vendored from foliate-js (https://github.com/johnfactotum/foliate-js) — MIT.
 * See LICENSE.foliate-js. Source kept verbatim in paginator.raw.js; this file
 * is generated by bundle.mjs and contains the same script embedded as a
 * single-line string for injection into a WebView. Do not hand-edit; re-run
 * bundle.mjs after updating paginator.raw.js.
 */
export const paginatorScript = ${JSON.stringify(paginatorSrc)}
`
writeFileSync(join(here, 'paginatorScript.ts'), paginatorOut)
console.log(`wrote paginatorScript.ts (${paginatorOut.length} bytes)`)

// ---------------------------------------------------------------------------
// bootstrapScript.ts — our highlights / selection / tap wiring around foliate
// ---------------------------------------------------------------------------
//
// Extracted from FoliateReader.tsx's inline template literal so comments and
// strings inside can use backticks freely (twice now I have closed the host
// literal by accident with a backtick in a comment). The IIFE was reshaped
// into `window.__foliateInit = (cfg, chapters, idx, frac) => { … }`; the
// host invokes it with one trailing `<script>` after this one loads.

const bootstrapRaw = readFileSync(join(here, 'bootstrap.raw.js'), 'utf8')

const bootstrapOut = `// biome-ignore-all lint/suspicious/noTemplateCurlyInString: bootstrap source — \${…} sequences run inside the WebView, not in this TS module.
// biome-ignore-all format: single-line embedded blob; reformatting would split it across thousands of lines.
/**
 * Foliate reader bootstrap. Source lives in bootstrap.raw.js; this file is
 * generated by bundle.mjs and contains the same script embedded as a single-
 * line string for injection into a WebView. Do not hand-edit; re-run
 * bundle.mjs after updating bootstrap.raw.js.
 */
export const bootstrapScript = ${JSON.stringify(bootstrapRaw)}
`
writeFileSync(join(here, 'bootstrapScript.ts'), bootstrapOut)
console.log(`wrote bootstrapScript.ts (${bootstrapOut.length} bytes)`)
