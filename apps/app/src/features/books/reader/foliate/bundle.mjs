#!/usr/bin/env node
// Wrap paginator.raw.js (the vendored MIT-licensed source from foliate-js)
// into paginatorScript.ts as a JSON-stringified constant. The TS module is
// imported by FoliateReader.tsx and injected as a <script> into the WebView.
//
// Re-run after updating paginator.raw.js:  node bundle.mjs

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const raw = readFileSync(join(here, 'paginator.raw.js'), 'utf8')

// Strip ES-module syntax so the script can run as a classic `<script>` inside
// a WebView whose document is `about:blank`. Module scripts get "Script error.
// @0" with CORS masking; classic scripts just run. The only top-level export
// is `export class Paginator`, and the side effect we actually want is the
// `customElements.define(...)` at the bottom, so dropping the `export` is
// sufficient.
//
// We also patch #onTouchMove to bail out (without calling preventDefault) when
// iOS native text selection is active — otherwise every drag of the selection
// handle calls scrollBy and the page wiggles. The line we anchor to is unique
// (the early-return guard for scrolled/pinched mode). Selection-active = a
// non-collapsed Range exists in the iframe's document.
const src = raw
  .replace(/^export\s+class\b/m, 'class')
  .replace(
    'if (this.scrolled || state.pinched) return',
    "if (this.scrolled || state.pinched) return; const __sel = e.target && e.target.ownerDocument && e.target.ownerDocument.getSelection && e.target.ownerDocument.getSelection(); if (__sel && __sel.type === 'Range' && !__sel.isCollapsed) return",
  )
  // Snappier page-turn animation — foliate ships 300ms; Apple Books and the
  // iOS short-transition standard feel closer to 200ms. Anchor on the unique
  // 3-arg slot of the `animate(...)` call inside the page-scroll path.
  .replace(
    'element[scrollProp], offset, 300, easeOutQuad',
    'element[scrollProp], offset, 200, easeOutQuad',
  )

const out = `// biome-ignore-all lint/suspicious/noTemplateCurlyInString: vendored foliate-js — \${…} sequences run inside the WebView, not in this TS module.
// biome-ignore-all format: single-line embedded blob; reformatting would split it across thousands of lines.
/**
 * Vendored from foliate-js (https://github.com/johnfactotum/foliate-js) — MIT.
 * See LICENSE.foliate-js. Source kept verbatim in paginator.raw.js; this file
 * is generated by bundle.mjs and contains the same script embedded as a
 * single-line string for injection into a WebView. Do not hand-edit; re-run
 * bundle.mjs after updating paginator.raw.js.
 */
export const paginatorScript = ${JSON.stringify(src)}
`

writeFileSync(join(here, 'paginatorScript.ts'), out)
console.log(`wrote paginatorScript.ts (${out.length} bytes)`)
