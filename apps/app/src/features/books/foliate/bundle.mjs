#!/usr/bin/env node
// Wrap paginator.raw.js (the vendored MIT-licensed source from foliate-js)
// into paginatorScript.ts as a JSON-stringified constant. The TS module is
// imported by FoliateReader.tsx and injected as a <script> into the WebView.
//
// Re-run after updating paginator.raw.js:  node bundle.mjs

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const src = readFileSync(join(here, 'paginator.raw.js'), 'utf8')

const out = `/**
 * Vendored from foliate-js (https://github.com/johnfactotum/foliate-js) — MIT.
 * See LICENSE.foliate-js. Source kept verbatim in paginator.raw.js; this file
 * is generated by bundle.mjs and contains the same script embedded as a
 * single-line string for injection into a WebView. Do not hand-edit; re-run
 * bundle.mjs after updating paginator.raw.js.
 */
export const paginatorScript = ${JSON.stringify(src)}
`

writeFileSync(join(here, 'paginatorScript.ts'), out)
console.log(`wrote paginatorScript.ts (${out.length} bytes)`)
